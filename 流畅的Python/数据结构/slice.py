l = [10, 20, 30, 40, 50, 60]
print(l[:2])  # 在下标2的地方分割
# 切片长度为2     (end-start)
# 切片把序列分为两个不重叠的部分  l[:2]  l[2:]
s = 'bicycle'
print(s[::2])
# 切片赋值
l = list(range(10))
l[2:5] = [20, 30]
print(l)
print(l[3::2])
# 如果赋值对象是一个切片，辅助语句的右侧必须是一个可迭代的对象
l[2:5] = [100]
print(l)

